/* A binary tree node

struct Node
{
    int data;
    struct Node* left;
    struct Node* right;
    
    Node(int x){
        data = x;
        left = right = NULL;
    }
};
 */

class Solution
{
    public:
    //Function to return the lowest common ancestor in a Binary Tree.
    Node* lca(Node* root ,int n1 ,int n2 )
    {
       // if root is NULL
       if(root == NULL) return root;

       // if root matches with any of the given n1 or n2
       // then it is the LCA
       if((root -> data == n1) || (root -> data == n2))
        return root;
    
        // now check which subtree contains the n1 and n2
        Node* leftLCA = lca(root -> left, n1, n2);
        Node* rightLCA = lca(root -> right, n1, n2);

        // if both leftLCA and rightLCA are not NULL
        // then one key is present in left subtree
        // and the other in right subtree
        if(leftLCA && rightLCA)
            return root;
        
        // check if one of them is NULL then return the other one
        return (leftLCA == NULL ? rightLCA : leftLCA);
    }
};

