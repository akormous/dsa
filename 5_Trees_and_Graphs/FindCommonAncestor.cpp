/*

Question: Find lowest common ancestor of two given nodes

What is Lowest Common Ancestor ?
The LCA between two nodes n1 and n2 is defined as the lowest node in the tree,
that has both n1 and n2 as descendants (where we allow a node to be a descendant of itself)

Approach:
NAIVE APPROACH
Store the paths from root to n1 and root to n2
Return the node where a mismatch happens
Time Complexity - O(n)

EFFICIENT APPROACH


*/
